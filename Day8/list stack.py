# Implementing Stack Using List
x = []

#.append here is used to add the names or elements to the List
x.append("Apple")
x.append("Banana")
x.append("cat")
x.append("Dog")

print("Starting stack")
print(x)

# .Pop here is used to remove the Lastly added  names or elements from the List
print("Names poped from stack")
print(x.pop())
print(x.pop())

# Using print(x) we can print the remaing names left in the List 
print("stack after names are poped")
print(x)

